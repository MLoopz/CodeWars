def send(s):
    code=''.join(format(ord(c),"b").zfill(7) for c in s)
    resp=""
    first=1
    last=0
    for n in code:
        if(first==1 or n!=last):
            last=n
            first=0
            if(n=='0'):
                resp+=' 00 0'
            else:
                resp+=' 0 0'
        else:
            resp+='0'
    return resp[1:]

